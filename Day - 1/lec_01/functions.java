/*
 * FUNCTIONS (METHODS) IN JAVA - THEORY
 * 
 * What is a Function?
 * - A function (also called a method in Java) is a block of code that performs a specific task
 * - Functions help in breaking down complex problems into smaller, manageable pieces
 * 
 * Why Use Functions?
 * - Code Reusability: Write once, use multiple times
 * - Better Organization: Makes code more readable and maintainable
 * - Easier Debugging: Isolate and fix problems in specific sections
 * - Modularity: Divide program into independent modules
 * 
 * Function Syntax:
 * accessModifier returnType functionName(parameters) {
 *     // function body
 *     return value; // if returnType is not void
 * }
 * 
 * Types of Functions:
 * 1. No parameters, No return type (void)
 * 2. With parameters, No return type (void)
 * 3. No parameters, With return type
 * 4. With parameters, With return type
 * 
 * Key Terms:
 * - Parameter: Variable in the function definition
 * - Argument: Actual value passed when calling the function
 * - Return: Sends a value back to the caller
 * - void: Indicates no return value
 */

